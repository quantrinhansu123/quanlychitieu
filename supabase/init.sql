to anon
using (user_id is null)
with check (user_id is null);

drop policy if exists "transactions_delete_global_anon" on public.transactions;
create policy "transactions_delete_global_anon"
on public.transactions
for delete
to anon
using (user_id is null);

drop policy if exists "transactions_insert_own" on public.transactions;
create policy "transactions_insert_own"
on public.transactions
for insert
to authenticated
with check (user_id = auth.uid());

drop policy if exists "transactions_update_own" on public.transactions;
create policy "transactions_update_own"
on public.transactions
for update
to authenticated
using (user_id = auth.uid())
with check (user_id = auth.uid());

drop policy if exists "transactions_delete_own" on public.transactions;
create policy "transactions_delete_own"
on public.transactions
for delete
to authenticated
using (user_id = auth.uid());

-- =========================
-- Budgets (optional)
-- =========================
create table if not exists public.budgets (
  id bigint generated by default as identity primary key,
  -- null = demo mode (no-auth). If you add auth later, store auth.uid() here.
  user_id uuid null references auth.users (id) on delete cascade,
  category_id bigint null references public.categories (id) on delete set null,
  month date not null, -- store as first day of month (e.g. 2026-05-01)
  limit_vnd bigint not null check (limit_vnd > 0),
  created_at timestamptz not null default now()
);

create unique index if not exists budgets_user_month_category_uniq
  on public.budgets (user_id, month, category_id);

create index if not exists budgets_user_month_idx
